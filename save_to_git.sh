#!/bin/bash
# Скрипт для добавления only_html директории в Git

# Добавляем папку в Git 
git add only_html/

# Создаем коммит с комментарием
git commit -m "Добавлена автономная HTML-версия игры без сервера"

# Отображаем статус после коммита
git status

echo "Папка only_html успешно добавлена в Git!"
echo "Для отправки изменений на удаленный репозиторий используйте команду: git push"